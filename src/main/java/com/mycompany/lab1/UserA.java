    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
String datos[] = {Txtname.getText(), Txtuser.getText(), Textcountry.getText(), Txtage.getText(), Cmbstatus.getSelectedItem().toString()};

if(datos[0].isEmpty() || datos[1].isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this, "Nombre y Usuario son obligatorios");
} else {
    javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblUsuarios.getModel();
    modelo.addRow(datos);
    // Limpiar campos
    Txtname.setText(""); Txtuser.setText(""); Textcountry.setText(""); Txtage.setText("");
}        // TODO add your handling code here:
    }//GEN-LAST:event_btAddActionPerformed

