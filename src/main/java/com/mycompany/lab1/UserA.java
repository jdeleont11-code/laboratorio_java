    private void btInactiveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInactiveActionPerformed
int fila = tblUsuarios.getSelectedRow();
if (fila >= 0) {
    tblUsuarios.setValueAt("Inactive", fila, 4); // Cambia la columna "St"
} else {
    javax.swing.JOptionPane.showMessageDialog(this, "Selecciona un usuario de la tabla");
}        // TODO add your handling code here:
    }//GEN-LAST:event_btInactiveActionPerformed

    private void btModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btModifyActionPerformed
    int fila = tblUsuarios.getSelectedRow();
    
    if (fila >= 0) {
        // 2. Actualizar cada columna con el nuevo texto de los campos
        tblUsuarios.setValueAt(Txtname.getText(), fila, 0);      // Columna Name
        tblUsuarios.setValueAt(Txtuser.getText(), fila, 1);      // Columna User
        tblUsuarios.setValueAt(Textcountry.getText(), fila, 2);  // Columna Country
        tblUsuarios.setValueAt(Txtage.getText(), fila, 3);       // Columna Age
        tblUsuarios.setValueAt(Cmbstatus.getSelectedItem().toString(), fila, 4); // Columna St
        
        javax.swing.JOptionPane.showMessageDialog(this, "¡Usuario actualizado con éxito!");
        
        // 3. Opcional: Limpiar campos después de modificar
        limpiarCampos(); 
        
    } else {
        // Si no hay fila seleccionada, avisar al usuario
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecciona primero un usuario de la tabla para modificar.");
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btModifyActionPerformed

